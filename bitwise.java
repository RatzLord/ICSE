class bitwise
{
    public static void main ()
    {
        int a,b;
        a=12;b=10;
        System.out.println("(a&b)="+(a&b));
        System.out.println("(a/b)="+(a/b));
        System.out.println("(a^b)="+(a^b));
        System.out.println("(a<<2)="+(a<<2));
        System.out.println("(-2>>1)="+(-2>>1));
        System.out.println("(-2>>>1)="+(-2>>>1));
    }
}